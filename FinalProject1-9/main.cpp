#include "users.h"
#include <windows.h>

using namespace std;

bool work = true;
bool authorisation = false;

int main() {
	SetConsoleCP(1251);
	SetConsoleOutputCP(1251);

	while (work) {
		cout << "1 - Вход. 2 - Регистрация. 3 - Справка. 0 - Выход." << endl;
		short userChoice;
		cin >> userChoice;

		switch (userChoice) {
		case 1:
			system("cls");
			cout << "Вход" << endl;
			signIn();
			if (authorisation) {
				//cout << user.getUserLogin() << " " << user.getUserName() << endl;
				//chat();
			}
			break;

		case 2:
			system("cls");
			cout << "Регистрация новго пользователя" << endl;
			signUp();
			break;

		case 3:
			system("cls");
			cout << "Справка" << endl;
			break;

		case 0:
			cout << "Выход" << endl;
			work = false;
			break;

		default:
			cout << "Введено неверное значение." << endl;
			break;
		}
	}

	return 0;
}